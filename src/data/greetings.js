// ── Public exports ─────────────────────────────────────────────────────────

/**
 * Flat array of pool metadata (backward-compatible with any code that
 * iterates GREETINGS and reads { from } from each entry).
 */
export const GREETINGS = GREETING_POOLS.map(({ from }) => ({ from }));

/**
 * Returns a greeting for the given 24-hour value.
 *
 * Selection is seeded by calendar day-of-year so the result is stable
 * across the per-second re-renders in the clock widget, and naturally
 * rotates to a fresh entry every midnight.
 *
 * @param {number} h24  Current hour (0–23)
 * @returns {{ from: number, prefix: string, label: string }}
 */
export const getGreeting = (h24) => {
  let matched = GREETING_POOLS[0];
  for (const slot of GREETING_POOLS) {
    if (h24 >= slot.from) matched = slot;
  }
  const pool = matched.greetings;

  // Stable within a day; advances at midnight.
  const now = new Date();
  const dayOfYear = Math.floor(
    (now - new Date(now.getFullYear(), 0, 0)) / 86_400_000,
  );

  const [prefix, label] = pool[dayOfYear % pool.length];
  return { from: matched.from, prefix, label };
};

